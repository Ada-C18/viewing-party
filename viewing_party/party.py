def create_movie(title, genre, rating):
    if title == False or genre == False or rating == False:
        return None
    else:
        movie = {
            "title" : title,
            "genre" : genre,
            "rating" : rating
        }
    return movie

def add_to_watched(user_data, movie):
    user_data["watched"].append(movie)
    return user_data



